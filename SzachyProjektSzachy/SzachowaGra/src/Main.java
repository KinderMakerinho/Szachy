public class Main {
    public static void main(String[] args) {
        System.out.println("Hello world!");
        liczDo10(); // Wywołanie funkcji liczDo10
    }

    // Funkcja licząca do 10
    public static void liczDo10() {
        for (int i = 1; i <= 15; i++) {
            System.out.println(i); // Wypisuje aktualną wartość i
        }
    }
}
